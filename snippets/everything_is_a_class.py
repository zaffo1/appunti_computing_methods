# Create an integer variable
this_is_an_int = 5
# Now check its type
print(type(this_is_an_int))

# Same with a string
this_is_a_string = 'Hello world!' 
print(type(this_is_a_string))

# Same with a list
this_is_a_list = ['Frodo', 'Samvise', 'Meriadoc', 'Peregrino']
print(type(this_is_a_list))

# And even a function!
def this_is_a_function():
    return 0
    
print(type(this_is_a_function))
